package generator

import (
	"slices"
	"strings"

	"github.com/romshark/datapages/parser/model"
)

// WritePkgAction generates code for the datapagesgen/action package
// and appends it to buffer.
func (w *Writer) WritePkgAction(m *model.App) {
	needsStrings := false

	// Check if any action uses path or query params (needs "strings" import).
	for _, a := range m.Actions {
		if a.InputPath != nil || a.InputQuery != nil {
			needsStrings = true
			break
		}
	}
	if !needsStrings {
		for _, p := range m.Pages {
			for _, a := range p.Actions {
				if a.InputPath != nil || a.InputQuery != nil {
					needsStrings = true
					break
				}
			}
			if needsStrings {
				break
			}
		}
	}

	w.writeActionHeader(needsStrings)

	// Collect all action entries for sorting.
	type actionEntry struct {
		funcName   string
		httpMethod string
		route      string
		pathInput  *model.Input
		queryInput *model.Input
	}

	var appActions []actionEntry
	for _, a := range m.Actions {
		funcName := strings.ToUpper(a.HTTPMethod) + "App" + a.Name
		appActions = append(appActions, actionEntry{
			funcName:   funcName,
			httpMethod: a.HTTPMethod,
			route:      a.Route,
			pathInput:  a.InputPath,
			queryInput: a.InputQuery,
		})
	}
	slices.SortFunc(appActions, func(a, b actionEntry) int {
		return strings.Compare(a.funcName, b.funcName)
	})

	var pageActions []actionEntry
	for _, p := range m.Pages {
		pageSuffix := stripPagePrefix(p.TypeName)
		for _, a := range p.Actions {
			funcName := strings.ToUpper(a.HTTPMethod) + "Page" + pageSuffix + a.Name
			pageActions = append(pageActions, actionEntry{
				funcName:   funcName,
				httpMethod: a.HTTPMethod,
				route:      a.Route,
				pathInput:  a.InputPath,
				queryInput: a.InputQuery,
			})
		}
	}
	slices.SortFunc(pageActions, func(a, b actionEntry) int {
		return strings.Compare(a.funcName, b.funcName)
	})

	// Emit App actions first, then page actions.
	for _, a := range appActions {
		w.writeActionFunc(a.funcName, a.httpMethod, a.route, a.queryInput)
	}
	for _, a := range pageActions {
		w.writeActionFunc(a.funcName, a.httpMethod, a.route, a.queryInput)
	}
}

func (w *Writer) writeActionHeader(needsStrings bool) {
	w.Line(0, "// Generated by github.com/romshark/datapages. DO NOT EDIT.")
	w.Line(0, "")
	w.Line(0, "// Package action provides generators for datastar action hypertext references.")
	w.Line(0, "// These references must be used in the templates.")
	w.Line(0, "package action")
	w.Line(0, "")
	if needsStrings {
		w.Line(0, `import "strings"`)
	}
}

// writeActionRouteComment writes a doc comment line like
// "// FuncName references /route/\n".
// It strips {$} and ensures a trailing slash.
func (w *Writer) writeActionRouteComment(funcName, route string) {
	w.Raw("// ")
	w.Raw(funcName)
	w.Raw(" references ")
	w.writeRouteURL(route)
	w.Byte('\n')
}

func (w *Writer) writeActionFunc(
	funcName string,
	httpMethod string,
	route string,
	queryInput *model.Input,
) {
	pathVars := routeVars(route)
	hasPathVars := len(pathVars) > 0

	var queryFields []structFieldInfo
	if queryInput != nil {
		queryFields = structFields(queryInput.Type.Resolved)
	}
	hasQuery := len(queryFields) > 0

	method := strings.ToLower(httpMethod)

	// Doc comment.
	w.Line(0, "")
	w.writeActionRouteComment(funcName, route)

	if !hasPathVars && !hasQuery {
		// Simple return with literal string.
		w.Linef(0, "func %s() string {", funcName)
		w.Raw("\treturn \"@")
		w.Raw(method)
		w.Raw("('")
		w.writeRouteURL(route)
		w.Raw("')\"\n")
		w.Line(0, "}")
		return
	}

	if hasPathVars && !hasQuery {
		w.writeActionFuncPathOnly(funcName, method, route, pathVars)
		return
	}

	if !hasPathVars && hasQuery {
		w.writeActionFuncQueryOnly(funcName, method, route, queryFields)
		w.writeActionQueryType(funcName, queryFields)
		return
	}

	// Both path and query.
	w.writeActionFuncPathAndQuery(funcName, method, route, pathVars, queryFields)
	w.writeActionQueryType(funcName, queryFields)
}

func (w *Writer) writeActionFuncPathOnly(
	funcName string,
	method string,
	route string,
	pathVars []string,
) {
	literals, _ := routeSegments(route)
	prefix := "@" + method + "('"
	suffix := "')"

	w.Linef(0, "func %s(%s) string {", funcName, joinParams(pathVars))
	w.Line(1, "var b strings.Builder")

	// Grow call.
	w.Line(1, "b.Grow("+
		"len("+quote(prefix+literals[0])+")"+
		buildPathGrowAdditions(pathVars, literals[1:], suffix)+
		")")

	// WriteString calls.
	w.Linef(1, "b.WriteString(%q)", prefix+literals[0])
	for i, v := range pathVars {
		w.Linef(1, "b.WriteString(%s)", v)
		nextLit := literals[i+1]
		if i == len(pathVars)-1 {
			w.Linef(1, "b.WriteString(%q)", nextLit+suffix)
		} else {
			w.Linef(1, "b.WriteString(%q)", nextLit)
		}
	}

	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func buildPathGrowAdditions(pathVars []string, nextLits []string, suffix string) string {
	var b strings.Builder
	for i, v := range pathVars {
		b.WriteString(" + len(")
		b.WriteString(v)
		b.WriteString(")")
		lit := nextLits[i]
		if i == len(pathVars)-1 {
			lit = lit + suffix
		}
		b.WriteString(" + len(")
		b.WriteString(quote(lit))
		b.WriteString(")")
	}
	return b.String()
}

func quote(s string) string {
	// small helper to avoid fmt usage; caller builds valid Go expression strings only
	// and uses %q elsewhere for emitted code lines.
	return `"` + strings.ReplaceAll(s, `"`, `\"`) + `"`
}

func (w *Writer) writeActionFuncQueryOnly(
	funcName string,
	method string,
	route string,
	fields []structFieldInfo,
) {
	w.Linef(0, "func %s(query Query%s) string {", funcName, funcName)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Builder and length calculation.
	w.Line(1, "var b strings.Builder")
	w.Raw("\tl := len(\"@")
	w.Raw(method)
	w.Raw("('")
	w.writeRouteURL(route)
	w.Raw("\") + len(\"')\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")

	// Query param length accumulation.
	w.Line(1, "n := 0")
	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.Linef(1, "if %s {", zeroCheck("query."+f.Name, f.Type))
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		w.Line(1, "}")
	}
	w.Line(1, "_ = n")
	w.Line(0, "")

	// Grow and write.
	w.Line(1, "b.Grow(l)")
	w.Line(0, "")
	w.Raw("\tb.WriteString(\"@")
	w.Raw(method)
	w.Raw("('")
	w.writeRouteURL(route)
	w.Raw("\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")

	// Write query params.
	w.Line(1, "n = 0")
	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.Linef(1, "if %s {", zeroCheck("query."+f.Name, f.Type))
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		w.Line(2, "_ = n")
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		w.Linef(2, "b.WriteString(query.%s)", f.Name)
		w.Line(1, "}")
	}

	w.Line(1, `b.WriteString("')")`)
	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeActionFuncPathAndQuery(
	funcName string,
	method string,
	route string,
	pathVars []string,
	fields []structFieldInfo,
) {
	literals, _ := routeSegments(route)
	prefix := "@" + method + "('"
	suffix := "')"

	w.Linef(0, "func %s(%s, query Query%s) string {", funcName, joinParams(pathVars), funcName)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Builder and length calculation.
	w.Line(1, "var b strings.Builder")

	// Path literal lengths.
	expr := "l := len(" + quote(prefix+literals[0]) + ")"
	for i, v := range pathVars {
		expr += " + len(" + v + ") + len(" + quote(literals[i+1]) + ")"
	}
	expr += " + len(" + quote(suffix) + ")"
	w.Line(1, expr)

	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")

	// Query param length accumulation.
	w.Line(1, "n := 0")
	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.Linef(1, "if %s {", zeroCheck("query."+f.Name, f.Type))
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		w.Line(1, "}")
	}
	w.Line(1, "_ = n")
	w.Line(0, "")

	// Grow and write path segments.
	w.Line(1, "b.Grow(l)")
	w.Line(0, "")
	w.Linef(1, "b.WriteString(%q)", prefix+literals[0])
	for i, v := range pathVars {
		w.Linef(1, "b.WriteString(%s)", v)
		w.Linef(1, "b.WriteString(%q)", literals[i+1])
	}

	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")

	// Write query params.
	w.Line(1, "n = 0")
	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.Linef(1, "if %s {", zeroCheck("query."+f.Name, f.Type))
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		w.Line(2, "_ = n")
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		w.Linef(2, "b.WriteString(query.%s)", f.Name)
		w.Line(1, "}")
	}

	w.Linef(1, "b.WriteString(%q)", suffix)
	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeActionQueryType(funcName string, fields []structFieldInfo) {
	w.Line(0, "")
	w.Linef(0, "type Query%s struct {", funcName)

	// Find longest field name for alignment.
	maxNameLen := 0
	for _, f := range fields {
		if len(f.Name) > maxNameLen {
			maxNameLen = len(f.Name)
		}
	}
	// Find longest type name for alignment.
	maxTypeLen := 0
	for _, f := range fields {
		typeName := fieldTypeName(f.Type)
		if len(typeName) > maxTypeLen {
			maxTypeLen = len(typeName)
		}
	}

	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		typeName := fieldTypeName(f.Type)
		w.Linef(1, "\t%-*s %-*s `query:\"%s\"`",
			maxNameLen, f.Name, maxTypeLen, typeName, tag)
	}

	w.Line(0, "}")
}
