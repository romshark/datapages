// WritePkgAction generates code for the datapagesgen/action package
// and appends it to buffer.
func (w *Writer) WritePkgAction(m *model.App) {
	needsStrconv := false

	// Count total page actions and check if any action uses path or query params.
	nPageActions := 0
	for _, a := range m.Actions {
		if a.InputPath != nil {
			if hasNonStringFields(w.structFields(a.InputPath.Type.Resolved)) {
				needsStrconv = true
			}
		}
		if a.InputQuery != nil {
			if hasNonStringFields(w.structFields(a.InputQuery.Type.Resolved)) {
				needsStrconv = true
			}
		}
	}
	for _, p := range m.Pages {
		nPageActions += len(p.Actions)
		for _, a := range p.Actions {
			if a.InputPath != nil {
				if hasNonStringFields(w.structFields(a.InputPath.Type.Resolved)) {
					needsStrconv = true
				}
			}
			if a.InputQuery != nil {
				if hasNonStringFields(w.structFields(a.InputQuery.Type.Resolved)) {
					needsStrconv = true
				}
			}
		}
	}

	hasActions := len(m.Actions) > 0 || nPageActions > 0
	w.writeActionHeader(hasActions, needsStrconv)

	type actionEntry struct {
		funcName   string
		httpMethod string
		route      string
		pathInput  *model.Input
		queryInput *model.Input
	}

	// Collect and sort app actions.
	appActions := make([]actionEntry, len(m.Actions))
	for i, a := range m.Actions {
		appActions[i] = actionEntry{
			funcName:   strings.ToUpper(a.HTTPMethod) + "App" + a.Name,
			httpMethod: a.HTTPMethod,
			route:      a.Route,
			pathInput:  a.InputPath,
			queryInput: a.InputQuery,
		}
	}
	slices.SortFunc(appActions, func(a, b actionEntry) int {
		return strings.Compare(a.funcName, b.funcName)
	})

	// Collect and sort page actions.
	pageActions := make([]actionEntry, 0, nPageActions)
	for _, p := range m.Pages {
		pageSuffix := stripPagePrefix(p.TypeName)
		for _, a := range p.Actions {
			pageActions = append(pageActions, actionEntry{
				funcName:   strings.ToUpper(a.HTTPMethod) + "Page" + pageSuffix + a.Name,
				httpMethod: a.HTTPMethod,
				route:      a.Route,
				pathInput:  a.InputPath,
				queryInput: a.InputQuery,
			})
		}
	}
	slices.SortFunc(pageActions, func(a, b actionEntry) int {
		return strings.Compare(a.funcName, b.funcName)
	})

	// Emit App actions first, then page actions.
	for _, a := range appActions {
		w.writeActionFunc(a.funcName, a.httpMethod, a.route, a.pathInput, a.queryInput)
	}
	for _, a := range pageActions {
		w.writeActionFunc(a.funcName, a.httpMethod, a.route, a.pathInput, a.queryInput)
	}
}

func (w *Writer) writeActionHeader(hasActions, needsStrconv bool) {
	w.Line(0, "// Code generated by github.com/romshark/datapages; DO NOT EDIT.")
	w.Line(0, "")
	w.Line(0, "// Package action provides generators for datastar action attribute expressions.")
	w.Line(0, "// Use these in templates instead of hardcoding action URLs.")
	w.Line(0, "package action")
	if !hasActions {
		return
	}
	w.Line(0, "")
	if needsStrconv {
		w.Line(0, "import (")
		w.Line(1, `"strconv"`)
		w.Line(1, `"strings"`)
		w.Line(0, ")")
	} else {
		w.Line(0, `import "strings"`)
	}
	w.Line(0, "")
	w.Line(0, "// Opt is the name of a Datastar action option.")
	w.Line(0, "type Opt string")
	w.Line(0, "")
	w.Line(0, "const (")
	w.Line(1, `OptContentType         Opt = "contentType"`)
	w.Line(1, `OptFilterSignals       Opt = "filterSignals"`)
	w.Line(1, `OptSelector            Opt = "selector"`)
	w.Line(1, `OptHeaders             Opt = "headers"`)
	w.Line(1, `OptOpenWhenHidden      Opt = "openWhenHidden"`)
	w.Line(1, `OptPayload             Opt = "payload"`)
	w.Line(1, `OptRetry               Opt = "retry"`)
	w.Line(1, `OptRetryInterval       Opt = "retryInterval"`)
	w.Line(1, `OptRetryScaler         Opt = "retryScaler"`)
	w.Line(1, `OptRetryMaxWaitMs      Opt = "retryMaxWaitMs"`)
	w.Line(1, `OptRetryMaxCount       Opt = "retryMaxCount"`)
	w.Line(1, `OptRequestCancellation Opt = "requestCancellation"`)
	w.Line(0, ")")
	w.Line(0, "")
	w.Line(0, "type option struct {")
	w.Line(1, "key   Opt")
	w.Line(1, "value string")
	w.Line(0, "}")
	w.Line(0, "")
	w.Line(0, "// WithOption creates an action option key-value pair.")
	w.Line(0, "// The value is a raw JavaScript expression")
	w.Line(0, `// (use "'auto'" for a JS string, "true" for a boolean).`)
	w.Line(0, "func WithOption(key Opt, value string) option {")
	w.Line(1, "return option{key: key, value: value}")
	w.Line(0, "}")
	w.Line(0, "")
	w.Line(0, "func writeOptions(b *strings.Builder, options []option) {")
	w.Line(1, `b.WriteString(", {")`)
	w.Line(1, "for i, o := range options {")
	w.Line(2, "if i > 0 {")
	w.Line(3, `b.WriteString(", ")`)
	w.Line(2, "}")
	w.Line(2, "b.WriteString(string(o.key))")
	w.Line(2, `b.WriteString(": ")`)
	w.Line(2, "b.WriteString(o.value)")
	w.Line(1, "}")
	w.Line(1, `b.WriteString("}")`)
	w.Line(0, "}")
	w.Line(0, "")
	w.Line(0, "func optionsLen(options []option) int {")
	w.Line(1, "if len(options) == 0 {")
	w.Line(2, "return 0")
	w.Line(1, "}")
	w.Line(1, `n := len(", {}")`)
	w.Line(1, "for i, o := range options {")
	w.Line(2, "if i > 0 {")
	w.Line(3, `n += len(", ")`)
	w.Line(2, "}")
	w.Line(2, `n += len(o.key) + len(": ") + len(o.value)`)
	w.Line(1, "}")
	w.Line(1, "return n")
	w.Line(0, "}")
}

// writeActionRouteComment writes a doc comment line like
// "// FuncName references /route/\n".
// It strips {$} and ensures a trailing slash.
func (w *Writer) writeActionRouteComment(funcName, route string) {
	w.Raw("// ")
	w.Raw(funcName)
	w.Raw(" references ")
	w.writeRouteURL(route)
	w.Byte('\n')
}

func (w *Writer) writeActionFunc(
	funcName string,
	httpMethod string,
	route string,
	pathInput *model.Input,
	queryInput *model.Input,
) {
	pathVars := slices.Collect(routepattern.Vars(route))
	hasPathVars := len(pathVars) > 0
	params := w.pathParamInfos(pathInput, pathVars)

	var queryFields []structFieldInfo
	if queryInput != nil {
		queryFields = w.structFields(queryInput.Type.Resolved)
	}
	hasQuery := len(queryFields) > 0

	method := strings.ToLower(httpMethod)

	// Doc comment.
	w.Line(0, "")
	w.writeActionRouteComment(funcName, route)

	if !hasPathVars && !hasQuery {
		w.Linef(0, "func %s(options ...option) string {", funcName)
		w.Line(1, "if len(options) == 0 {")
		w.Raw("\t\treturn \"@")
		w.Raw(method)
		w.Raw("('")
		w.writeRouteURL(route)
		w.Raw("')\"\n")
		w.Line(1, "}")
		w.Line(1, "var b strings.Builder")
		w.Raw("\tb.Grow(len(\"@")
		w.Raw(method)
		w.Raw("('")
		w.writeRouteURL(route)
		w.Raw("'\") + optionsLen(options) + len(\")\")")
		w.Raw(")\n")
		w.Raw("\tb.WriteString(\"@")
		w.Raw(method)
		w.Raw("('")
		w.writeRouteURL(route)
		w.Raw("'\")\n")
		w.Line(1, "writeOptions(&b, options)")
		w.Line(1, "b.WriteByte(')')")
		w.Line(1, "return b.String()")
		w.Line(0, "}")
		return
	}

	if hasPathVars && !hasQuery {
		w.writeActionFuncPathOnly(funcName, method, route, params)
		return
	}

	if !hasPathVars && hasQuery {
		w.writeActionFuncQueryOnly(funcName, method, route, queryFields)
		w.writeActionQueryType(funcName, queryFields)
		return
	}

	// Both path and query.
	w.writeActionFuncPathAndQuery(funcName, method, route, params, queryFields)
	w.writeActionQueryType(funcName, queryFields)
}

func (w *Writer) writeActionFuncPathOnly(
	funcName string,
	method string,
	route string,
	params []pathParamInfo,
) {
	literals, _ := routeSegments(route)

	// func FuncName(params, options ...option) string {
	w.Raw("func ")
	w.Raw(funcName)
	w.Byte('(')
	w.writeTypedParams(params)
	w.Raw(", options ...option) string {\n")

	// Pre-convert non-string params to strings.
	w.writePathPreConvert(params)

	w.Line(1, "var b strings.Builder")

	// b.Grow(len("@method('lit0") + len(v0) + ... + len("litN'") + optionsLen + len(")"))
	w.Raw("\tb.Grow(len(\"@")
	w.Raw(method)
	w.Raw("('")
	w.Raw(literals[0])
	w.Raw("\")")
	for i, p := range params {
		w.Raw(" + len(")
		w.Raw(pathVarStrExpr(p))
		w.Raw(") + len(\"")
		w.Raw(literals[i+1])
		if i == len(params)-1 {
			w.Raw("'")
		}
		w.Raw("\")")
	}
	w.Raw(" + optionsLen(options) + len(\")\")")
	w.Raw(")\n")

	// b.WriteString("@method('lit0")
	w.Raw("\tb.WriteString(\"@")
	w.Raw(method)
	w.Raw("('")
	w.Raw(literals[0])
	w.Raw("\")\n")
	for i, p := range params {
		w.Raw("\tb.WriteString(")
		w.Raw(pathVarStrExpr(p))
		w.Raw(")\n")
		w.Raw("\tb.WriteString(\"")
		w.Raw(literals[i+1])
		if i == len(params)-1 {
			w.Raw("'")
		}
		w.Raw("\")\n")
	}

	w.Line(1, "writeOptions(&b, options)")
	w.Line(1, "b.WriteByte(')')")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeActionFuncQueryOnly(
	funcName string,
	method string,
	route string,
	fields []structFieldInfo,
) {
	// func FuncName(query QueryFuncName, options ...option) string {
	w.Raw("func ")
	w.Raw(funcName)
	w.Raw("(query Query")
	w.Raw(funcName)
	w.Raw(", options ...option) string {\n")

	// Pre-convert non-string fields to strings.
	w.writeQueryPreConvert(fields)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Builder and length calculation.
	w.Line(1, "var b strings.Builder")
	w.Raw("\tl := len(\"@")
	w.Raw(method)
	w.Raw("('")
	w.writeRouteURL(route)
	w.Raw("'\") + optionsLen(options) + len(\")\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")

	// Query param length accumulation.
	w.Line(1, "n := 0")
	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		if isStringType(f.Type) {
			w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		} else {
			w.Linef(2, "l += len(%q) + len(%sStr)", tag+"=", tag)
		}
		w.Line(1, "}")
	}
	w.Line(0, "")

	// Grow and write.
	w.Line(1, "b.Grow(l)")
	w.Line(0, "")
	w.Raw("\tb.WriteString(\"@")
	w.Raw(method)
	w.Raw("('")
	w.writeRouteURL(route)
	w.Raw("\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")

	// Write query params.
	w.Line(1, "n = 0")
	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		if isStringType(f.Type) {
			w.Linef(2, "b.WriteString(query.%s)", f.Name)
		} else {
			w.Linef(2, "b.WriteString(%sStr)", tag)
		}
		w.Line(1, "}")
	}

	w.Line(1, `b.WriteString("'")`)
	w.Line(1, "writeOptions(&b, options)")
	w.Line(1, "b.WriteByte(')')")
	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeActionFuncPathAndQuery(
	funcName string,
	method string,
	route string,
	params []pathParamInfo,
	fields []structFieldInfo,
) {
	literals, _ := routeSegments(route)

	// func FuncName(params, query QueryFuncName, options ...option) string {
	w.Raw("func ")
	w.Raw(funcName)
	w.Byte('(')
	w.writeTypedParams(params)
	w.Raw(", query Query")
	w.Raw(funcName)
	w.Raw(", options ...option) string {\n")

	// Pre-convert non-string path params.
	w.writePathPreConvert(params)

	// Pre-convert non-string query fields to strings.
	w.writeQueryPreConvert(fields)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Builder and length calculation.
	w.Line(1, "var b strings.Builder")

	// l := len("@method('lit0") + len(v0) + len("lit1") + ... + len("')")
	w.Raw("\tl := len(\"@")
	w.Raw(method)
	w.Raw("('")
	w.Raw(literals[0])
	w.Raw("\")")
	for i, p := range params {
		w.Raw(" + len(")
		w.Raw(pathVarStrExpr(p))
		w.Raw(") + len(\"")
		w.Raw(literals[i+1])
		w.Raw("\")")
	}
	w.Raw(" + len(\"'\") + optionsLen(options) + len(\")\")\n")

	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")

	// Query param length accumulation.
	w.Line(1, "n := 0")
	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		if isStringType(f.Type) {
			w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		} else {
			w.Linef(2, "l += len(%q) + len(%sStr)", tag+"=", tag)
		}
		w.Line(1, "}")
	}
	w.Line(0, "")

	// Grow and write path segments.
	w.Line(1, "b.Grow(l)")
	w.Line(0, "")
	w.Raw("\tb.WriteString(\"@")
	w.Raw(method)
	w.Raw("('")
	w.Raw(literals[0])
	w.Raw("\")\n")
	for i, p := range params {
		w.Raw("\tb.WriteString(")
		w.Raw(pathVarStrExpr(p))
		w.Raw(")\n")
		w.Raw("\tb.WriteString(\"")
		w.Raw(literals[i+1])
		w.Raw("\")\n")
	}

	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")

	// Write query params.
	w.Line(1, "n = 0")
	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		if isStringType(f.Type) {
			w.Linef(2, "b.WriteString(query.%s)", f.Name)
		} else {
			w.Linef(2, "b.WriteString(%sStr)", tag)
		}
		w.Line(1, "}")
	}

	w.Line(1, `b.WriteString("'")`)
	w.Line(1, "writeOptions(&b, options)")
	w.Line(1, "b.WriteByte(')')")
	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeActionQueryType(funcName string, fields []structFieldInfo) {
	w.Line(0, "")
	w.Raw("type Query")
	w.Raw(funcName)
	w.Raw(" struct {\n")

	// Find longest field name for alignment.
	maxNameLen := 0
	for _, f := range fields {
		if len(f.Name) > maxNameLen {
			maxNameLen = len(f.Name)
		}
	}
	// Find longest type name for alignment.
	maxTypeLen := 0
	for _, f := range fields {
		typeName := fieldTypeName(f.Type)
		if len(typeName) > maxTypeLen {
			maxTypeLen = len(typeName)
		}
	}

	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		typeName := fieldTypeName(f.Type)
		w.Linef(1, "\t%-*s %-*s `query:\"%s\"`",
			maxNameLen, f.Name, maxTypeLen, typeName, tag)
	}

	w.Line(0, "}")
}
