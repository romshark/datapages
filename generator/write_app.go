func handlerKind(h *model.Handler) string {
	if h.HTTPMethod == "" || h.HTTPMethod == "GET" {
		return ""
	}
	return "action"
}

func actionOwnerName(p *model.Page, isAppLevel bool) string {
	if isAppLevel {
		return "App"
	}
	if p != nil {
		return p.TypeName
	}
	return "App"
}

// writeGETCall generates the GET method call and HTML rendering for a page.
func (w *Writer) writeGETCall(p *model.Page, m *model.App, appPkg string, context string) {
	if p.GET == nil || p.GET.OutputBody == nil {
		return
	}
	h := p.GET.Handler

	// Build output list.
	var outsBuf [8]string
	outs := outsBuf[:0]
	if p.GET.OutputBody != nil {
		outs = append(outs, p.GET.OutputBody.Name)
	}
	if p.GET.OutputHead != nil {
		outs = append(outs, p.GET.OutputHead.Name)
	}
	if h.OutputRedirect != nil {
		outs = append(outs, h.OutputRedirect.Name)
	}
	if h.OutputDisableRefresh != nil {
		outs = append(outs, h.OutputDisableRefresh.Name)
	}
	if h.OutputEnableBgStream != nil {
		outs = append(outs, h.OutputEnableBgStream.Name)
	}
	if h.OutputErr != nil {
		outs = append(outs, "err")
	}

	// Build input args in user-defined order.
	args := handlerInputArgs(h, false)

	w.Byte('\t')
	w.writeCommaSep(outs)
	w.Raw(" := ")
	w.writeCallExpr("p", "GET", args)
	w.Byte('\n')

	if h.OutputErr != nil {
		w.Line(1, "if err != nil {")
		w.Raw("\t\ts.httpErrIntern(w, r, nil, \"handling ")
		w.Raw(p.TypeName)
		w.Raw(".GET\", err)\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Redirect.
	if h.OutputRedirect != nil {
		w.Raw("\tif httpRedirect(w, r, ")
		w.Raw(h.OutputRedirect.Name)
		w.Raw(", 0) {\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Generic head.
	if m.GlobalHeadGenerator != nil {
		hasSess := h.InputSession != nil || h.InputSessionToken != nil
		hasSessToken := h.InputSessionToken != nil
		w.writeGenericHeadCall(m.GlobalHeadGenerator, appPkg, hasSess, hasSessToken)
	}

	// Body attrs - simple for render404/error pages.
	w.Line(0, "")
	w.Line(1, "bodyAttrs := func(w http.ResponseWriter) {")
	w.Line(2, "writeBodyAttrOnVisibilityChange(w)")
	w.Line(1, "}")

	headArg := "nil"
	if p.GET.OutputHead != nil {
		headArg = p.GET.OutputHead.Name
	}

	w.Line(1, "if err := s.writeHTML(")
	w.Raw("\t\tw, r, ")
	if m.Session != nil {
		sessArg := "sess"
		headNeedsSession := m.GlobalHeadGenerator != nil &&
			(m.GlobalHeadGenerator.InputSession || m.GlobalHeadGenerator.InputSessionToken)
		if p.PageSpecialization == model.PageTypeError500 ||
			(p.PageSpecialization == model.PageTypeError404 && context == "render404" && !headNeedsSession) {
			sessArg = appPkg + ".Session{}"
		}
		w.Raw(sessArg)
		w.Raw(", ")
	}
	if m.GlobalHeadGenerator != nil {
		w.Raw("genericHead, ")
	}
	w.Raw(headArg)
	w.Raw(", body, bodyAttrs,\n")
	w.Line(1, "); err != nil {")
	w.Raw("\t\ts.logErr(\"rendering ")
	w.Raw(p.TypeName)
	w.Raw("\", err)\n")
	w.Line(2, "return")
	w.Line(1, "}")
}
