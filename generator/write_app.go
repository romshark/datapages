func handlerKind(h *model.Handler) string {
	if h.HTTPMethod == "" || h.HTTPMethod == "GET" {
		return ""
	}
	return "action"
}

func actionOwnerName(p *model.Page, isAppLevel bool) string {
	if isAppLevel {
		return "App"
	}
	if p != nil {
		return p.TypeName
	}
	return "App"
}

// writeGETCall generates the GET method call and HTML rendering for a page.
func (w *Writer) writeGETCall(p *model.Page, appPkg string, context string) {
	if p.GET == nil {
		return
	}
	h := p.GET.Handler

	// Build output list.
	var outsBuf [8]string
	outs := outsBuf[:0]
	if p.GET.OutputBody != nil {
		outs = append(outs, p.GET.OutputBody.Name)
	}
	if p.GET.OutputHead != nil {
		outs = append(outs, p.GET.OutputHead.Name)
	}
	if h.OutputRedirect != nil {
		outs = append(outs, h.OutputRedirect.Name)
	}
	if h.OutputDisableRefresh != nil {
		outs = append(outs, h.OutputDisableRefresh.Name)
	}
	if h.OutputEnableBgStream != nil {
		outs = append(outs, h.OutputEnableBgStream.Name)
	}
	if h.OutputErr != nil {
		outs = append(outs, "err")
	}

	// Build input args.
	var argsBuf [8]string
	args := argsBuf[:0]
	if h.InputRequest != nil {
		args = append(args, "r")
	}
	if h.InputSession != nil {
		args = append(args, "sess")
	}
	if h.InputPath != nil {
		args = append(args, "path")
	}
	if h.InputQuery != nil {
		args = append(args, "query")
	}

	w.Byte('\t')
	w.writeCommaSep(outs)
	w.Raw(" := ")
	w.writeCallExpr("p", "GET", args)
	w.Byte('\n')

	if h.OutputErr != nil {
		w.Line(1, "if err != nil {")
		w.Raw("\t\ts.httpErrIntern(w, r, nil, \"handling ")
		w.Raw(p.TypeName)
		w.Raw(".GET\", err)\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Redirect.
	if h.OutputRedirect != nil {
		w.Raw("\tif httpRedirect(w, r, ")
		w.Raw(h.OutputRedirect.Name)
		w.Raw(", 0) {\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Generic head.
	w.Line(1, "genericHead, err := s.app.Head(r)")
	w.Line(1, "if err != nil {")
	w.Raw("\t\ts.httpErrIntern(w, r, nil, \"generating generic head for ")
	w.Raw(p.TypeName)
	w.Raw("\", err)\n")
	w.Line(2, "return")
	w.Line(1, "}")

	// Body attrs - simple for render404/error pages.
	w.Line(0, "")
	w.Line(1, "bodyAttrs := func(w http.ResponseWriter) {")
	w.Line(2, "writeBodyAttrOnVisibilityChange(w)")
	w.Line(1, "}")

	// writeHTML call.
	sessArg := "sess"
	if p.PageSpecialization == model.PageTypeError500 {
		sessArg = appPkg + ".Session{}"
	} else if p.PageSpecialization == model.PageTypeError404 && context == "render404" {
		sessArg = appPkg + ".Session{}"
	}

	headArg := "nil"
	if p.GET.OutputHead != nil {
		headArg = p.GET.OutputHead.Name
	}

	w.Line(1, "if err := s.writeHTML(")
	w.Raw("\t\tw, r, ")
	w.Raw(sessArg)
	w.Raw(", genericHead, ")
	w.Raw(headArg)
	w.Raw(", body, bodyAttrs,\n")
	w.Line(1, "); err != nil {")
	w.Raw("\t\ts.logErr(\"rendering ")
	w.Raw(p.TypeName)
	w.Raw("\", err)\n")
	w.Line(2, "return")
	w.Line(1, "}")
}
