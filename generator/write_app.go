func (w *Writer) writeHandlerCallAndOutputs(
	p *model.Page, h *model.Handler, m *model.App, appPkg string, isAppLevel bool,
) {
	// Body size limit for non-GET actions.
	if h.InputSignals != nil && h.InputSSE == nil {
		w.Line(1, "r.Body = http.MaxBytesReader(w, r.Body, DefaultBodySizeLimit)")
	}

	// Read signals.
	if h.InputSignals != nil {
		w.Raw("\tvar signals ")
		w.Raw(renderSignalsType(h.InputSignals, m))
		w.Byte('\n')
		w.Line(1, "if err := datastar.ReadSignals(r, &signals); err != nil {")
		w.Line(2, `s.httpErrBad(w, "reading signals", err)`)
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Read query params.
	if h.InputQuery != nil {
		w.writeReadQuery(h.InputQuery, m)
	}

	// Read path params.
	if h.InputPath != nil {
		w.writeReadPath(h.InputPath, m)
	}

	// Dispatch closure.
	if h.InputDispatch != nil {
		w.writeDispatchClosure(h.InputDispatch, appPkg)
	}

	// SSE for actions that take it.
	if h.InputSSE != nil && !isAppLevel {
		w.Line(0, "")
		w.Line(1, "sse := datastar.NewSSE(w, r, datastar.WithCompression())")
	}

	// Page constructor (for page actions).
	if p != nil {
		w.writePageConstructorStmt("p", p, appPkg)
	}

	// Build the actual method call.
	w.writeMethodCall(p, h, m, appPkg, isAppLevel)
}

func (w *Writer) writeMethodCall(
	p *model.Page, h *model.Handler, m *model.App, appPkg string, isAppLevel bool,
) {
	// Build output variable list.
	var outsBuf [8]string
	outs := outsBuf[:0]
	if h.OutputBody != nil {
		outs = append(outs, h.OutputBody.Name)
	}
	if h.OutputCloseSession != nil {
		outs = append(outs, h.OutputCloseSession.Name)
	}
	if h.OutputRedirect != nil {
		outs = append(outs, h.OutputRedirect.Name)
	}
	if h.OutputRedirectStatus != nil {
		outs = append(outs, h.OutputRedirectStatus.Name)
	}
	if h.OutputNewSession != nil {
		outs = append(outs, h.OutputNewSession.Name)
	}
	if h.OutputEnableBgStream != nil {
		outs = append(outs, h.OutputEnableBgStream.Name)
	}
	if h.OutputDisableRefresh != nil {
		outs = append(outs, h.OutputDisableRefresh.Name)
	}
	if h.OutputErr != nil {
		outs = append(outs, "err")
	}

	// Build input args in user-defined order.
	args := handlerInputArgs(h, isAppLevel)

	// Build the call expression.
	receiver := "p"
	if isAppLevel {
		receiver = "s.app"
	}
	methodName := h.HTTPMethod + h.Name

	if len(outs) == 0 {
		// Void return or only error.
		if h.OutputErr != nil {
			w.Raw("\tif err := ")
			w.writeCallExpr(receiver, methodName, args)
			w.Raw("; err != nil {\n")
			w.Raw("\t\ts.httpErrIntern(w, r, nil, \"handling action ")
			w.Raw(actionOwnerName(p, isAppLevel))
			w.Byte('.')
			w.Raw(h.Name)
			w.Raw("\", err)\n")
			w.Line(1, "}")
		} else {
			w.Byte('\t')
			w.writeCallExpr(receiver, methodName, args)
			w.Byte('\n')
		}
		return
	}

	w.Byte('\t')
	w.writeCommaSep(outs)
	w.Raw(" := ")
	w.writeCallExpr(receiver, methodName, args)
	w.Byte('\n')

	if h.OutputErr != nil {
		sseRef := "nil"
		if h.InputSSE != nil && !isAppLevel {
			sseRef = "sse"
		}
		w.Line(1, "if err != nil {")
		w.Raw("\t\ts.httpErrIntern(w, r, ")
		w.Raw(sseRef)
		w.Raw(", \"handling ")
		w.Raw(handlerKind(h))
		if handlerKind(h) != "" {
			w.Byte(' ')
		}
		w.Raw(actionOwnerName(p, isAppLevel))
		w.Byte('.')
		w.Raw(h.Name)
		w.Raw("\", err)\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Close session.
	if h.OutputCloseSession != nil {
		w.Raw("\tif ")
		w.Raw(h.OutputCloseSession.Name)
		w.Raw(" {\n")
		w.Line(2, "if err := s.closeSession(w, r, sessToken); err != nil {")
		w.Line(3, `s.httpErrIntern(w, r, nil, "removing session", err)`)
		w.Line(3, "return")
		w.Line(2, "}")
		w.Line(1, "}")
	}

	// New session.
	if h.OutputNewSession != nil {
		w.Raw("\tif j := ")
		w.Raw(h.OutputNewSession.Name)
		w.Raw("; j.UserID != \"\" {\n")
		w.Raw("\t\tif err := s.createSession(w, r, ")
		w.Raw(h.OutputNewSession.Name)
		w.Raw("); err != nil {\n")
		w.Line(3, `s.httpErrIntern(w, r, nil, "creating session", err)`)
		w.Line(2, "}")
		w.Line(1, "}")
	}

	// Redirect.
	if h.OutputRedirect != nil {
		statusArg := "0"
		if h.OutputRedirectStatus != nil {
			statusArg = h.OutputRedirectStatus.Name
		}
		w.Raw("\tif httpRedirect(w, r, ")
		w.Raw(h.OutputRedirect.Name)
		w.Raw(", ")
		w.Raw(statusArg)
		w.Raw(") {\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}

	// Render body (if action returns templ.Component).
	if h.OutputBody != nil {
		ownerName := actionOwnerName(p, isAppLevel)

		if m.GlobalHeadGenerator != nil {
			w.writeGenericHeadCall(m.GlobalHeadGenerator, appPkg, hasSessionInput(h), false)
		}

		w.Line(1, "if err := s.writeHTML(")
		w.Raw("\t\tw, r, ")
		if m.Session != nil {
			sessArg := "sess"
			headNeedsSession := m.GlobalHeadGenerator != nil &&
				(m.GlobalHeadGenerator.InputSession || m.GlobalHeadGenerator.InputSessionToken)
			if !hasSessionInput(h) && !headNeedsSession {
				sessArg = appPkg + ".Session{}"
			}
			w.Raw(sessArg)
			w.Raw(", ")
		}
		if m.GlobalHeadGenerator != nil {
			w.Raw("genericHead, nil, ")
		} else {
			w.Raw("nil, ")
		}
		w.Raw(h.OutputBody.Name)
		w.Raw(", nil,\n")
		w.Line(1, "); err != nil {")
		w.Raw("\t\ts.logErr(\"rendering response of ")
		w.Raw(ownerName)
		w.Byte('.')
		w.Raw(h.HTTPMethod)
		w.Raw(h.Name)
		w.Raw("\", err)\n")
		w.Line(2, "return")
		w.Line(1, "}")
	}
}

func (w *Writer) writeDispatchClosure(d *model.InputDispatch, appPkg string) {
	w.Line(0, "")
	w.Line(1, "dispatch := func(")
	for i, evName := range d.EventTypeNames {
		w.Raw("\t\te")
		w.Raw(itoa(i + 1))
		w.Byte(' ')
		w.Raw(appPkg)
		w.Byte('.')
		w.Raw(evName)
		w.Raw(",\n")
	}
	w.Line(1, ") error {")

	for i, evName := range d.EventTypeNames {
		idx := itoa(i + 1)
		ev := w.eventMap[evName]
		w.Line(2, "{")
		w.Raw("\t\t\tj, err := json.Marshal(e")
		w.Raw(idx)
		w.Raw(")\n")
		w.Line(3, "if err != nil {")
		w.Raw("\t\t\t\treturn fmt.Errorf(\"marshaling ")
		w.Raw(evName)
		w.Raw(" JSON: %w\", err)\n")
		w.Line(3, "}")

		if ev != nil && ev.HasTargetUserIDs {
			w.Raw("\t\t\tfor _, uid := range e")
			w.Raw(idx)
			w.Raw(".TargetUserIDs {\n")
			w.Raw("\t\t\t\tsubj := ")
			w.Raw(evSubjPrefConst(ev))
			w.Raw(" + uid\n")
			w.Line(4, "err = s.messageBroker.Publish(r.Context(), s.messageBrokerMetrics, subj, j)")
			w.Line(4, "if err != nil {")
			w.Line(5, "return fmt.Errorf(\"publishing subject %q: %w\", subj, err)")
			w.Line(4, "}")
			w.Line(3, "}")
		} else if ev != nil {
			w.Raw("\t\t\terr = s.messageBroker.Publish(r.Context(), s.messageBrokerMetrics, ")
			w.Raw(evSubjConst(ev))
			w.Raw(", j)\n")
			w.Line(3, "if err != nil {")
			w.Raw("\t\t\t\treturn fmt.Errorf(\"publishing subject %q: %w\", ")
			w.Raw(evSubjConst(ev))
			w.Raw(", err)\n")
			w.Line(3, "}")
		}
		w.Line(2, "}")
	}

	w.Line(2, "return nil")
	w.Line(1, "}")
}

func renderSignalsType(input *model.Input, m *model.App) string {
	if isNamedType(input.Type) {
		return renderType(input.Type, m.PkgPath)
	}
	return renderAnonStructType(input.Type, m.Fset)
}

