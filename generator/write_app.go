//go:embed app_static.go.txt
var appStaticContent string

// WriteApp generates code for the generated root package and appends it to buffer.
// pkgName is the Go package name (e.g. "datapagesgen").
func (w *Writer) WriteApp(pkgName string, m *model.App) {
	appPkg := appPkgName(m.PkgPath)
	w.buildEventMap(m.Events)

	w.writeAppHeader(pkgName, m.PkgPath, needsJSON(m))
	w.Raw(appStaticContent)
	w.writeAppCheckCSRF(appPkg)
	w.writeAppWriteHTML(m, appPkg)
	w.writeAppHandleStreamRequest(appPkg)
	w.writeAppServerStruct(appPkg)
	w.writeAppNewServer(appPkg)
	w.writeEventSubjectConsts(m.Events)
	w.writeMessageBrokerStreamSubjects(m.Events)
	w.writeEvSubjPageFuncs(m.Pages)
	w.writeAppAuth(appPkg)
	w.writeBrokerSubjectKind(m.Events)
	w.writeSetupHandlers(m)
	w.writeAppErrHelpers(m, appPkg)

	if m.PageError404 != nil {
		w.writeRender404(m, appPkg)
	}

	// App-level action handlers.
	for _, h := range m.Actions {
		w.writeAppActionHandler(h, m, appPkg)
	}

	// Per-page handlers.
	for _, p := range m.Pages {
		if p.GET != nil {
			w.writePageGETHandler(p, m, appPkg)
		}
		if pageHasStream(p) {
			w.writePageGETStreamHandler(p, m, appPkg)
			if pageHasAnonStream(p, w.eventMap) {
				w.writePageGETStreamAnonHandler(p, appPkg)
			}
		}
		for _, h := range p.Actions {
			w.writePageActionHandler(p, h, m, appPkg)
		}
	}
}

func needsJSON(m *model.App) bool {
	for _, h := range m.Actions {
		if h.InputDispatch != nil {
			return true
		}
	}
	for _, p := range m.Pages {
		for _, h := range p.Actions {
			if h.InputDispatch != nil {
				return true
			}
		}
	}
	return false
}

