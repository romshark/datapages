// WritePkgHref generates code for the datapagesgen/href package
// and appends it to buffer.
func (w *Writer) WritePkgHref(m *model.App) {
	needsStrconv, needsStrings := false, false
	for _, p := range m.Pages {
		if p.PageSpecialization == model.PageTypeError500 {
			continue
		}
		if p.GET == nil {
			continue
		}
		pathVars := slices.Collect(routepattern.Vars(p.Route))
		if len(pathVars) > 0 {
			needsStrings = true
			// Check for non-string path fields.
			if p.GET.Handler != nil && p.GET.InputPath != nil {
				if hasNonStringFields(w.structFields(p.GET.InputPath.Type.Resolved)) {
					needsStrconv = true
				}
			}
		}
		if p.GET.Handler != nil && p.GET.InputQuery != nil {
			needsStrings = true
			if hasNonStringFields(w.structFields(p.GET.InputQuery.Type.Resolved)) {
				needsStrconv = true
			}
		}
	}

	w.writeHrefHeader(needsStrconv, needsStrings)

	for _, p := range m.Pages {
		if p.PageSpecialization == model.PageTypeError500 {
			continue
		}
		if p.GET == nil {
			continue
		}
		w.writeHrefFunc(p)
	}
}

func (w *Writer) writeHrefHeader(needsStrconv, needsStrings bool) {
	w.Line(0, "// Code generated by github.com/romshark/datapages; DO NOT EDIT.")
	w.Line(0, "")
	w.Line(0, "// Package href provides generators for datastar anchor hypertext references.")
	w.Line(0, "// Use these in templates instead of hardcoding URLs.")
	w.Line(0, "package href")
	w.Line(0, "")

	switch {
	case needsStrconv && needsStrings:
		w.Line(0, "import (")
		w.Line(1, `"strconv"`)
		w.Line(1, `"strings"`)
		w.Line(0, ")")
	case needsStrings:
		w.Line(0, `import "strings"`)
	}
}

// writeRouteComment writes a doc comment line like "// FuncName references /route/{$}\n".
// It ensures the route ends with {$} for exact matching display.
func (w *Writer) writeRouteComment(funcName, route string) {
	w.Raw("// ")
	w.Raw(funcName)
	w.Raw(" references ")
	if strings.HasSuffix(route, "{$}") {
		w.Raw(route)
	} else if route == "/" {
		w.Raw("/{$}")
	} else {
		w.Raw(route)
		w.Raw("/{$}")
	}
	w.Byte('\n')
}

// writeRouteURL writes the URL for a route with no path variables to the buffer.
// It strips {$} and ensures a trailing slash.
func (w *Writer) writeRouteURL(route string) {
	r := strings.TrimSuffix(route, "{$}")
	r = strings.TrimSuffix(r, "/")
	if r == "" {
		w.Byte('/')
		return
	}
	w.Raw(r)
	w.Byte('/')
}

// routeSegments splits a route with path variables into alternating
// literal and variable segments. The returned slices satisfy:
// URL = literals[0] + vars[0] + literals[1] + vars[1] + ... + literals[len(literals)-1]
// where len(literals) == len(vars) + 1.
func routeSegments(route string) (literals []string, vars []string) {
	// Remove trailing {$} marker.
	r := strings.TrimSuffix(route, "{$}")
	r = strings.TrimSuffix(r, "/")

	for {
		i := strings.IndexByte(r, '{')
		if i < 0 {
			// Remaining literal; ensure trailing slash.
			if r == "" {
				literals = append(literals, "/")
			} else {
				literals = append(literals, r+"/")
			}
			break
		}
		// Literal before the variable.
		literals = append(literals, r[:i])
		r = r[i+1:]

		j := strings.IndexByte(r, '}')
		varName := strings.TrimSuffix(r[:j], "...")
		if varName != "$" && varName != "" {
			vars = append(vars, varName)
		}
		r = r[j+1:]
	}

	return literals, vars
}

func (w *Writer) writeHrefFunc(p *model.Page) {
	funcName := pageNameForHref(p.TypeName)
	pathVars := slices.Collect(routepattern.Vars(p.Route))
	hasPathVars := len(pathVars) > 0

	// Build typed path param info.
	var pathInput *model.Input
	if p.GET != nil && p.GET.Handler != nil {
		pathInput = p.GET.InputPath
	}
	params := w.pathParamInfos(pathInput, pathVars)

	var querySt *types.Struct
	var queryFields []structFieldInfo
	if p.GET != nil && p.GET.Handler != nil && p.GET.InputQuery != nil {
		if st, ok := p.GET.InputQuery.Type.Resolved.Underlying().(*types.Struct); ok {
			querySt = st
			queryFields = w.structFields(querySt)
		}
	}
	hasQuery := len(queryFields) > 0

	// Doc comment.
	w.Line(0, "")
	w.writeRouteComment(funcName, p.Route)

	if !hasPathVars && !hasQuery {
		// Simple one-liner.
		w.Raw("func ")
		w.Raw(funcName)
		w.Raw("() string { return \"")
		w.writeRouteURL(p.Route)
		w.Raw("\" }\n")
		return
	}

	// Build function signature.
	if hasPathVars && !hasQuery {
		// Path-only function.
		w.writeHrefFuncPathOnly(funcName, p.Route, params)
	} else if !hasPathVars && hasQuery {
		// Query-only function.
		w.writeHrefFuncQueryOnly(funcName, p.Route, queryFields)
		w.writeHrefQueryType(funcName, querySt)
	} else {
		// Both path and query params.
		w.writeHrefFuncPathAndQuery(funcName, p.Route, params, queryFields)
		w.writeHrefQueryType(funcName, querySt)
	}
}

func (w *Writer) writeHrefFuncPathOnly(funcName, route string, params []pathParamInfo) {
	// Function signature with path params.
	w.Raw("func ")
	w.Raw(funcName)
	w.Byte('(')
	w.writeTypedParams(params)
	w.Raw(") string {\n")

	// Pre-convert non-string params to strings.
	w.writePathPreConvert(params)

	literals, _ := routeSegments(route)

	// Builder.
	w.Line(1, "var b strings.Builder")
	w.Line(1, "b.Grow(")
	for i, lit := range literals {
		if i == 0 {
			w.Linef(2, "len(%q) +", lit)
		} else if i < len(literals)-1 {
			w.Linef(3, "len(%s) +", pathVarStrExpr(params[i-1]))
			w.Linef(3, "len(%q) +", lit)
		} else {
			w.Linef(3, "len(%s) +", pathVarStrExpr(params[i-1]))
			w.Linef(3, "len(%q),", lit)
		}
	}
	w.Line(1, ")")

	// Write segments.
	for i, lit := range literals {
		w.Linef(1, "b.WriteString(%q)", lit)
		if i < len(params) {
			w.Linef(1, "b.WriteString(%s)", pathVarStrExpr(params[i]))
		}
	}

	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeHrefFuncQueryOnly(
	funcName, route string, fields []structFieldInfo,
) {
	w.Linef(0, "func %s(query Query%s) string {", funcName, funcName)

	// Pre-convert non-string fields to strings.
	w.writeQueryPreConvert(fields)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Length calculation.
	w.Line(1, "var b strings.Builder")
	w.Raw("\tl := len(\"")
	w.writeRouteURL(route)
	w.Raw("\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")
	w.Line(0, "")

	// n counter for & separator.
	w.Line(1, `// n = number of query params already accounted for (for '&')`)
	w.Line(1, "n := 0")
	w.Line(0, "")

	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		if isStringType(f.Type) {
			w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		} else {
			w.Linef(2, "l += len(%q) + len(%sStr)", tag+"=", tag)
		}
		w.Line(1, "}")
	}
	w.Line(1, "_ = n")
	w.Line(0, "")

	w.Line(1, "b.Grow(l)")
	w.Line(0, "")

	// Write URL base.
	w.Raw("\tb.WriteString(\"")
	w.writeRouteURL(route)
	w.Raw("\")\n")
	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")
	w.Line(0, "")

	// Reset n and write query params.
	w.Line(1, "n = 0")
	w.Line(0, "")

	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		// Only increment n if not the last field.
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		if isStringType(f.Type) {
			w.Linef(2, "b.WriteString(query.%s)", f.Name)
		} else {
			w.Linef(2, "b.WriteString(%sStr)", tag)
		}
		w.Line(1, "}")
	}

	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeHrefFuncPathAndQuery(
	funcName, route string,
	params []pathParamInfo, fields []structFieldInfo,
) {
	// Function signature with path params + query struct.
	w.Raw("func ")
	w.Raw(funcName)
	w.Byte('(')
	w.writeTypedParams(params)
	w.Raw(", query Query")
	w.Raw(funcName)
	w.Raw(") string {\n")

	literals, _ := routeSegments(route)

	// Pre-convert non-string path params.
	w.writePathPreConvert(params)

	// Pre-convert non-string query fields to strings.
	w.writeQueryPreConvert(fields)

	// anyQuery check.
	w.writeAnyCheck("anyQuery", fields)
	w.Line(0, "")

	// Length calculation.
	w.Line(1, "var b strings.Builder")

	// Path literal lengths.
	if len(literals) > 0 {
		// First line.
		w.Linef(1, "l := len(%q)", literals[0])
		// Remaining path literals and vars.
		for i := 1; i < len(literals); i++ {
			// preceding var length exists for i-1
			if i-1 < len(params) {
				w.Linef(2, "+ len(%s)", pathVarStrExpr(params[i-1]))
			}
			w.Linef(2, "+ len(%q)", literals[i])
		}
		// Add last var length if routeSegments produced one extra var (shouldn't).
		if len(params) >= len(literals) {
			w.Linef(2, "+ len(%s)", pathVarStrExpr(params[len(literals)-1]))
		}
	} else {
		w.Line(1, "l := 0")
	}

	w.Line(1, "if anyQuery {")
	w.Line(2, `l += len("?")`)
	w.Line(1, "}")
	w.Line(0, "")

	w.Line(1, `// n = number of query params already accounted for (for '&')`)
	w.Line(1, "n := 0")
	w.Line(0, "")

	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `l += len("&")`)
		w.Line(2, "}")
		w.Line(2, "n++")
		if isStringType(f.Type) {
			w.Linef(2, "l += len(%q) + len(query.%s)", tag+"=", f.Name)
		} else {
			w.Linef(2, "l += len(%q) + len(%sStr)", tag+"=", tag)
		}
		w.Line(1, "}")
	}
	w.Line(1, "_ = n")
	w.Line(0, "")

	w.Line(1, "b.Grow(l)")
	w.Line(0, "")

	// Write path segments.
	for i, lit := range literals {
		w.Linef(1, "b.WriteString(%q)", lit)
		if i < len(params) {
			w.Linef(1, "b.WriteString(%s)", pathVarStrExpr(params[i]))
		}
	}

	w.Line(1, "if anyQuery {")
	w.Line(2, `b.WriteString("?")`)
	w.Line(1, "}")
	w.Line(0, "")

	w.Line(1, "n = 0")
	w.Line(0, "")

	for i, f := range fields {
		tag := queryTagValue(f.Tag)
		w.writeIfZeroCheck(1, "query."+f.Name, f.Type)
		w.Line(2, "if n > 0 {")
		w.Line(3, `b.WriteString("&")`)
		w.Line(2, "}")
		if i < len(fields)-1 {
			w.Line(2, "n++")
		}
		w.Linef(2, "b.WriteString(%q)", tag+"=")
		if isStringType(f.Type) {
			w.Linef(2, "b.WriteString(query.%s)", f.Name)
		} else {
			w.Linef(2, "b.WriteString(%sStr)", tag)
		}
		w.Line(1, "}")
	}

	w.Line(0, "")
	w.Line(1, "return b.String()")
	w.Line(0, "}")
}

func (w *Writer) writeHrefQueryType(funcName string, st *types.Struct) {
	w.Line(0, "")
	w.Linef(0, "// Query%s is the query parameters for %s", funcName, funcName)
	w.Linef(0, "type Query%s struct {", funcName)

	fields := w.structFields(st)

	// Find longest field name for alignment.
	maxNameLen := 0
	for _, f := range fields {
		if len(f.Name) > maxNameLen {
			maxNameLen = len(f.Name)
		}
	}
	// Find longest type name for alignment.
	maxTypeLen := 0
	for _, f := range fields {
		typeName := fieldTypeName(f.Type)
		if len(typeName) > maxTypeLen {
			maxTypeLen = len(typeName)
		}
	}

	for _, f := range fields {
		tag := queryTagValue(f.Tag)
		typeName := fieldTypeName(f.Type)
		w.Linef(1, "\t%-*s %-*s `query:\"%s\"`",
			maxNameLen, f.Name, maxTypeLen, typeName, tag)
	}

	w.Line(0, "}")
}

// pathParamInfo describes a path variable with its route name and Go type.
type pathParamInfo struct {
	Name   string     // route variable name (e.g. "value")
	Type   types.Type // nil means string
	StrVar string     // variable name for string representation (e.g. "valueStr")
}

// pathParamInfos builds typed path parameter info from a path input and route variables.
// It computes conflict-free StrVar names for non-string parameters.
func (w *Writer) pathParamInfos(pathInput *model.Input, pathVars []string) []pathParamInfo {
	infos := make([]pathParamInfo, len(pathVars))
	if pathInput == nil {
		for i, v := range pathVars {
			infos[i] = pathParamInfo{Name: v, StrVar: v}
		}
		return infos
	}
	fields := w.structFields(pathInput.Type.Resolved)
	tagToType := make(map[string]types.Type, len(fields))
	for _, f := range fields {
		if tag := pathTagValue(f.Tag); tag != "" {
			tagToType[tag] = f.Type
		}
	}
	for i, v := range pathVars {
		infos[i] = pathParamInfo{Name: v, Type: tagToType[v]}
	}

	// Compute conflict-free StrVar for each parameter.
	// Non-string params need a string conversion variable named "s_<name>".
	// If that collides with another param name, prepend another "s_" prefix.
	names := make(map[string]bool, len(infos))
	for _, info := range infos {
		names[info.Name] = true
	}
	for i := range infos {
		if infos[i].Type == nil || isStringType(infos[i].Type) {
			infos[i].StrVar = infos[i].Name
		} else {
			candidate := "s_" + infos[i].Name
			for names[candidate] {
				candidate = "s_" + candidate
			}
			infos[i].StrVar = candidate
			names[candidate] = true
		}
	}
	return infos
}

// pathVarStrExpr returns the expression for a path variable's string representation.
func pathVarStrExpr(p pathParamInfo) string {
	return p.StrVar
}

// writeTypedParams writes a comma-separated typed parameter list.
func (w *Writer) writeTypedParams(params []pathParamInfo) {
	for i, p := range params {
		if i > 0 {
			w.Raw(", ")
		}
		w.Raw(p.Name)
		w.Byte(' ')
		if p.Type == nil || isStringType(p.Type) {
			w.Raw("string")
		} else {
			w.Raw(fieldTypeName(p.Type))
		}
	}
}

// writePathPreConvert emits string conversion for non-string path parameters.
func (w *Writer) writePathPreConvert(params []pathParamInfo) {
	for _, p := range params {
		if p.Type == nil || isStringType(p.Type) {
			continue
		}
		w.Byte('\t')
		w.Raw(p.StrVar)
		w.Raw(" := ")
		w.writeFormatExpr(p.Name, p.Type)
		w.Byte('\n')
	}
}

// writeFormatExpr emits a strconv.Format* expression for a non-string type.
func (w *Writer) writeFormatExpr(varName string, t types.Type) {
	if isIntType(t) {
		_, unsigned := intTypeParseInfo(t)
		typeName := intTypeName(t)
		if unsigned {
			if typeName != "uint64" {
				w.Rawf("strconv.FormatUint(uint64(%s), 10)", varName)
			} else {
				w.Rawf("strconv.FormatUint(%s, 10)", varName)
			}
		} else {
			if typeName != "int64" {
				w.Rawf("strconv.FormatInt(int64(%s), 10)", varName)
			} else {
				w.Rawf("strconv.FormatInt(%s, 10)", varName)
			}
		}
	} else if isFloatType(t) {
		bits := floatBits(t)
		typeName := floatTypeName(t)
		if typeName != "float64" {
			w.Rawf("strconv.FormatFloat(float64(%s), 'f', -1, %d)", varName, bits)
		} else {
			w.Rawf("strconv.FormatFloat(%s, 'f', -1, %d)", varName, bits)
		}
	} else if isBoolType(t) {
		w.Rawf("strconv.FormatBool(%s)", varName)
	}
}

// writeZeroCheck writes the zero-value check expression for a field to the buffer.
func (w *Writer) writeZeroCheck(expr string, t types.Type) {
	w.Raw(expr)
	if isBoolType(t) {
		// bool: the expression itself is the condition
		return
	}
	if isStringType(t) {
		w.Raw(` != ""`)
	} else {
		// int, float: != 0
		w.Raw(" != 0")
	}
}

// writeIfZeroCheck writes "if <expr> != <zero> {\n" at the given indent level.
func (w *Writer) writeIfZeroCheck(indent int, expr string, t types.Type) {
	for range indent {
		w.Byte('\t')
	}
	w.Raw("if ")
	w.writeZeroCheck(expr, t)
	w.Raw(" {\n")
}

// fieldTypeName returns the Go type name for a field type.
// For types not directly representable (e.g. TextUnmarshaler), returns "string".
func fieldTypeName(t types.Type) string {
	if isIntType(t) {
		return intTypeName(t)
	}
	if isFloatType(t) {
		return floatTypeName(t)
	}
	if isBoolType(t) {
		return "bool"
	}
	return "string"
}

// writeQueryPreConvert emits variable declarations and conversion code
// for non-string query fields.
func (w *Writer) writeQueryPreConvert(fields []structFieldInfo) {
	if !hasNonStringFields(fields) {
		return
	}
	w.Line(1, "var (")
	for _, f := range fields {
		if !isStringType(f.Type) {
			tag := queryTagValue(f.Tag)
			w.Linef(2, "%sStr string", tag)
		}
	}
	w.Line(1, ")")
	w.Line(0, "")
	for _, f := range fields {
		if isStringType(f.Type) {
			continue
		}
		tag := queryTagValue(f.Tag)
		w.Raw("\tif ")
		w.writeZeroCheck("query."+f.Name, f.Type)
		w.Raw(" {\n")
		w.Rawf("\t\t%sStr = ", tag)
		w.writeFormatExpr("query."+f.Name, f.Type)
		w.Byte('\n')
		w.Line(1, "}")
	}
	w.Line(0, "")
}

// hasNonStringFields reports whether any field has a non-string type.
func hasNonStringFields(fields []structFieldInfo) bool {
	for _, f := range fields {
		if !isStringType(f.Type) {
			return true
		}
	}
	return false
}
